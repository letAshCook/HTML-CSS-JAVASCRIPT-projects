var currentQuestion = 0;
var score = 0;
var countdown;

// Display question and options
function displayQuestion() {
    var q = questions[currentQuestion];
    document.getElementById("question").innerHTML = q.question;
    var optionsHTML = "";
    for (var i = 0; i < q.options.length; i++) {
        optionsHTML += '<label><input type="radio" name="answer" value="' + q.options[i] + '"> ' + q.options[i] + '</label>';
    }
    document.getElementById("options").innerHTML= optionsHTML;
}

// Check user's answer and update score
function checkAnswer69() {
    var answer = document.querySelector('input[name="answer"]:checked');
    if (answer !== null) {
        if (answer.value === questions[currentQuestion].answer) {
            score++;
        }
        currentQuestion++;
        if (currentQuestion < questions.length) {
            displayQuestion();
        } else {
            endQuiz();
        }
    } else if(answer == -1){
        document.getElementById("feedback").innerHTML = "Please select an answer!";
    }
}

// End quiz and display score
function endQuiz() {
    clearInterval(countdown);
    var question = document.getElementById("question")
    question.style.textAlign = "center";
    question.style.fontWeight = "bold"
    question.innerHTML = "Quiz Completed";
    document.getElementById("options").innerHTML = "";
    document.getElementById("submit").style.display = "none";
    document.getElementById("feedback").innerHTML = "You scored " + score + " out of " + questions.length + ".";
    document.getElementById("restart").style.display = "block";
}

// Start quiz and hide start button
function startQuiz() {
    document.getElementById("start").style.display = "none";
    displayQuestion();
    var timer = document.getElementById("countdown");
    var minutes = 30; // change this to the desired number of minutes
    var seconds = 0;
    var totalSeconds = minutes * 60;
    timer.innerHTML = formatTime(minutes) + ":" + formatTime(seconds);
    countdown = setInterval(function() {
        if (seconds == 0) {
            minutes--;
            seconds = 59;
        } else {
            seconds--;
        }
        timer.innerHTML = formatTime(minutes) + ":" + formatTime(seconds);
        if (minutes == 0 && seconds == 0) {
            endQuiz();
        }
    }, 1000); // change this to 1000 to decrement by 1 second
}


function formatTime(time) {
    return time < 10 ? "0" + time : time;
}

